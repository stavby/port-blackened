import os
import shutil

def delete_node_modules(root_dir):
    for root, dirs, files in os.walk(root_dir):
        if 'node_modules' in dirs:
            dir_path = os.path.join(root, 'node_modules')
            try:
                shutil.rmtree(dir_path)
                print(f"Deleted {dir_path}")
            except OSError as e:
                print(f"Error deleting {dir_path}: {e}")

# Call the function with the root directory you want to search
root_dir = os.getcwd()
delete_node_modules(root_dir)